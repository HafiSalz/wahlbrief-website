function sendEmailRequest() {
  const email = elements.email.value
  if (!email) {
    return
  }
  const wantsNewsletter = elements.newsletter.checked
  const state = selectedPlace.state.toLowerCase()
  const newsletterEndPoint = `https://fragdenstaat.de/newsletter/update/wahlbrief-${state}/subscribe-ajax/`
  const answer = Math.ceil(Math.PI * 2)

  const userData = new URLSearchParams({
    ars: selectedPlace.ars,
    state: selectedPlace.state,
    postcode: selectedPlace.ZIP,
    place: selectedPlace.ORT,
    newsletter: wantsNewsletter ? "1" : "",
  })

  const data = new URLSearchParams({
    email: email,
    test: answer.toString(),
    data: userData.toString()
  });
  fetch(newsletterEndPoint, {
      method: "POST",
      mode: "cors",
      headers: {
          "Content-Type": "application/x-www-form-urlencoded",
          "x-requested-with": "XMLHttpRequest"
      },
      body: data,
  }).then(res => {
      if (res.ok) {
          return res.text();
      } else {
        console.error("Bad response from reminder service")
      }
  }).then(text => {
      console.log("Good response from reminder service")
  }).catch(e => {
      console.error(e);
      console.error("Failed to send data.");
  })
}

if (elements.newsletter) {
  elements.newsletter.addEventListener("change", () => {
    if (elements.newsletter.checked) {
      elements.email.setAttribute("required", "")
    } else {
      elements.email.removeAttribute("required")
    }
  })
}
if (elements.mainform) {
  elements.mainform.addEventListener("submit", (e) => {
    e.preventDefault()
    if (!elements.mainform.checkValidity()) {
      return
    }
    sendEmailRequest()
    thirdpage()
  })
}