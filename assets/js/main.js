function is_valid_datalist_value(inputValue, cityValue) {
  onEnter(); // Selects the item even if suggestion is not selected
  const filtered = zips.filter(
    (item) =>
      item.PLZ.toString() == inputValue &&
      item.ORT.toString() === cityValue &&
      (selectedEmail ? item["E-Mail"].toString() == selectedEmail : true)
  );

  if (filtered.length > 0) {
    const data = filtered[0];
    selectedPlace = data
    updateTextContent({
      munname: data.ORT,
      munemail: atob(data["E-Mail"]),
      muniname: data.ORT,
      muniemail: atob(data["E-Mail"]),
      instructionemail: atob(data["E-Mail"]),
    });

    formElements.city.value = data.ORT;
    formElements.zip.value = data.PLZ;


    // Hide all
    document.querySelectorAll("#election-hints .election-hint").forEach((el) => {el.style.display = "none"})
    selectedElection = elections.filter(e => e.state === data.state)[0]
    if (selectedElection == undefined) {
      document.getElementById("election-hint-none").style.display = "block"
    } else {
      const MIN_DAYS = 6
      const day = 1000 * 60 * 60 * 24
      const todayDate = new Date()
      const today = todayDate.getTime()
      const electionTime = selectedElection.date.getTime()
      const shippingDateMin = Math.max(today + (5 * day), electionTime - (30 * day))
      const shippingDateMax = Math.max(Math.max(shippingDateMin, today + (21 * day)), electionTime - (MIN_DAYS * day))
      const daysLeft = Math.floor((electionTime - today) / day)
      let arrival = `${formatDate(shippingDateMin)} – ${formatDate(shippingDateMax)}`
      document.getElementById("arrival-date").innerText = arrival
      if (daysLeft < MIN_DAYS) {
        document.getElementById("election-hint-late").style.display = "block"
      } else {
        document.getElementById("election-hint-normal").style.display = "block"
      }
    }

    formElements.zip.dispatchEvent(
      new Event("input", { bubbles: true, cancellable: true })
    );
    formElements.city.dispatchEvent(
      new Event("input", { bubbles: true, cancellable: true })
    );
    return true;
  }
  return false;
}

let activeSuggestionIndex = -1;

function setFocusOnSuggestion() {
  const suggestionItems = Array.from(elements.suggestions.children);
  const numSuggestions = suggestionItems.length;

  if (numSuggestions === 0) return;

  if (activeSuggestionIndex >= 0) {
    activeSuggestionIndex %= numSuggestions;
    suggestionItems.forEach((child) =>
      child.classList.remove("autocomplete-active")
    );
    suggestionItems[activeSuggestionIndex].classList.add("autocomplete-active");
  }
}

function onEnter() {
  const suggestionItems = Array.from(elements.suggestions.children);
  const numSuggestions = suggestionItems.length;

  if (numSuggestions === 0) return;

  if (numSuggestions === 1) {
    activeSuggestionIndex %= numSuggestions;
    suggestionItems[activeSuggestionIndex].click();
    setTimeout(secondpage, 300);
  }

  if (activeSuggestionIndex > 1) {
    activeSuggestionIndex %= numSuggestions;
    suggestionItems[activeSuggestionIndex].click();
    elements.searchInput.addEventListener("keyup", (e) => {
      if (e.keyCode === 13) {
        e.target.removeEventListener("keyup", arguments.callee);
        secondpage();
        activeSuggestionIndex = -1;
      }
    });
  }
}

let suggestionTimeout;

elements.searchInput?.addEventListener("keyup", (e) => {
  suggestionTimeout && clearTimeout(suggestionTimeout);

  suggestionTimeout = setTimeout(() => {
    switch (e.keyCode) {
      case 40: // Down arrow
        activeSuggestionIndex++;
        setFocusOnSuggestion();
        return;
      case 38: // Up arrow
        activeSuggestionIndex--;
        setFocusOnSuggestion();
        return;
      case 13: // Enter
        onEnter();
        return;
      case 27: // Escape
        e.target.value = "";
        elements.suggestions.innerHTML = "";
        return;
    }

    activeSuggestionIndex = -1;

    if (!e.target.value) {
      elements.suggestions.innerHTML = "";
      return;
    }

    const matchArray = findMatches(e.target.value, zips);
    const suggestionDivs = matchArray.map((place) => {
      const div = document.createElement("div");
      const nameSpan = document.createElement("span");
      const value = `${place.PLZ} ${place.ORT}`;

      nameSpan.innerText = value;
      div.appendChild(nameSpan);

      elements.searchInput.scrollIntoView();

      div.addEventListener("click", () => {
        elements.searchInput.value = value;
        selectedEmail = place["E-Mail"];
        selectedPlace = place
        elements.suggestions.innerHTML = "";
      });

      return div;
    });

    if (matchArray.length <= 0) {
        elements.suggestions.innerHTML =
          "<div class='error'><i class='fas fa-times-circle me-2'></i><span>" + getTranslation("right.invalidZipcode") + "</span></div>";
      return;
    }

    elements.suggestions.innerHTML = "";
    elements.suggestions.append(...suggestionDivs);
  }, 50);
});

